public class OrderDetailDAO {

  String order_id;
  String cloth_code;
  int item_count;
  String order_date;
  String order_stage;

  public String getOrder_stage() {
    return order_stage;
  }

  public void setOrder_stage(String order_stage) {
    this.order_stage = order_stage;
  }

  public String getOrder_id() {
    return order_id;
  }

  public void setOder_id(String order_id) {
    this.order_id = order_id;
  }

  public void setOrder_id(String order_id) {
    this.order_id = order_id;
  }

  public String getCloth_code() {
    return cloth_code;
  }

  public void setCloth_code(String cloth_code) {
    this.cloth_code = cloth_code;
  }

  public int getItem_count() {
    return item_count;
  }

  public void setItem_count(int item_count) {
    this.item_count = item_count;
  }

  public String getOrder_date() {
    return order_date;
  }

  public void setOrder_date(String order_date) {
    this.order_date = order_date;
  }
}
