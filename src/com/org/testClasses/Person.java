package com.org.testClasses;

public class Person {

  private String name;
  private String age;

  public Person() {}

  public Person(String name, String age) {
    this.name = name;
    this.age = age;
  }

  public String getAge() {
    return age;
  }

  public void setAge(String age) {
    this.age = age;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
